function getErrorPageGameMap(): GameMap {
    const unCollidableRed: Tile = { ...getDefaultTile("Red Tile"), canCollide: false }
    return GameMap.fromNumberMatrix("ERROR", ERROR_PAGE_MAP_MATRIX, {
        0: getDefaultTile("Empty Tile"),
        1: unCollidableRed,
        2: getDefaultTile("Red Tile")
    })
}


